package mypackage;

public class ArithmeticOpertors {

	public static void main(String[] args) {
		int a=7;
		int b=4;
		int s=a+b;
		int t=a-b;
		int m=a*b;
		int d=a/b;
		int r=a%b;
		System.out.println(a+"+"+b+"="+s);
		System.out.println(a+"-"+b+"="+t);
		System.out.println(a+"*"+b+"="+m);
		System.out.println(a+"/"+b+"="+d);
		System.out.println(a+"%"+b+"="+r);
	

	}

}
